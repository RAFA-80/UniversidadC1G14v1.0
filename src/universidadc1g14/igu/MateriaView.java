    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        // TODO add your handling code here:
            if (idMateria.getText() != null) {
            int id = Integer.parseInt(idMateria.getText());
            String nomb = nombre.getText();
            int a = Integer.parseInt(anio.getText());
            boolean activo = cbEstado.isEnabled();
            Materia materia = new Materia(id,nomb, a, activo);
            materiaData.modificarMateria(materia);
        }

    }//GEN-LAST:event_actualizarActionPerformed

    private void limpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_limpiarActionPerformed
        idMateria.setText("");
        nombre.setText("");
        anio.setText("");
        cbEstado.doClick();
        
    }//GEN-LAST:event_limpiarActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(idMateria.getText());
        Materia m = materiaData.buscarMateria(id);
        if(m != null){
            idMateria.setText(m.getIdMateria()+"");
            nombre.setText(m.getNombre());
            anio.setText(m.getAnio()+"");
            cbEstado.setSelected(m.isEstado());
        }
    }//GEN-LAST:event_buscarActionPerformed

    private void guardarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBActionPerformed
        // TODO add your handling code here:
        String nom = nombre.getText();
        int a = Integer.parseInt(anio.getText());
        boolean activo = cbEstado.isEnabled();
        Materia materia = new Materia(nom, a, activo);
        materiaData.guardarMateria(materia);
        idMateria.setText(materia.getIdMateria() + "");
    }//GEN-LAST:event_guardarBActionPerformed

    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(idMateria.getText());
        materiaData.eliminarMateria(id);
    }//GEN-LAST:event_borrarActionPerformed


